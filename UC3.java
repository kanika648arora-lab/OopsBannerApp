public class UC3 {

    public static void main(String[] args) {

        // Printing OOPS Banner using String.join()

        System.out.println(String.join(" ", 
                "  *  ", "  **  ", " **  "));

        System.out.println(String.join(" ", 
                " *  * ", " *   * ", " *   * "));

        System.out.println(String.join(" ", 
                "*    *", "*    *", "*    *"));

        System.out.println(String.join(" ", 
                "*    *", "*    *", " ***  "));

        System.out.println(String.join(" ", 
                "*    *", "*    *", "    **   "));

        System.out.println(String.join(" ", 
                " *  * ", " *   * ", " *  *  "));

        System.out.println(String.join(" ", 
                "  *  ", "  **  ", " **  "));
    }
}